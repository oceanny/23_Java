package com.java.inheritance;

import java.util.Scanner;

public class 措傈 extends 蜡己备 {

	private String[] 备;

	public void 备八祸(String 备) {
		super.林家 += 备;
	}

	@Override
	public String 林家() {
		Scanner sc = new Scanner(System.in);

		System.out.print("备 甫 涝仿 : ");
		String input_备 = sc.nextLine() + "\t";
		
		备八祸(input_备);
		
		return super.林家();
	}

	public String[] get备() {
		return 备;
	}
	
	
}
